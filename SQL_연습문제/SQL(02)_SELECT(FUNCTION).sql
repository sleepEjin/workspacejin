--1. 영어영문학과 학생들의 학번과 이름, 입학년도를 입학년도가 빠른 순서대로 표시
SELECT STUDENT_NO, STUDENT_NAME, ENTRANCE_DATE
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '영어영문학과'
ORDER BY ENTRANCE_DATE;

--2. 춘 기술대학교의 교수 중 이름이 세글자가 아닌 교수의 이름과 주민번호
SELECT PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE PROFESSOR_NAME NOT LIKE '___';

--3. 춘기술대학교의 남자교수들의 이름과 나이를 출력
-- 나이가 적은 순에서 많은 순으로, 나이는 만으로 계산
SELECT PROFESSOR_NAME, EXTRACT(YEAR FROM SYSDATE) - (19 || SUBSTR(PROFESSOR_SSN, 1, 2)) "나이"
FROM TB_PROFESSOR
ORDER BY 나이;

--4. 교수들의 이름 중 성을 제외한 이름만 출력
SELECT SUBSTR(PROFESSOR_NAME,2) "이름"
FROM TB_PROFESSOR;

--5. 춘 기술대학교의 재수생 입학자
SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE EXTRACT(YEAR  FROM ENTRANCE_DATE) - EXTRACT(YEAR FROM TO_DATE(SUBSTR(STUDENT_SSN, 1, 6))) > 19;

SELECT EXTRACT(YEAR FROM TO_DATE(SUBSTR(STUDENT_SSN, 1, 6)))
FROM TB_STUDENT;

--6. 2020년 크리스마스는 무슨 요일인가
SELECT TO_CHAR(TO_DATE('20201225'), 'DAY')
