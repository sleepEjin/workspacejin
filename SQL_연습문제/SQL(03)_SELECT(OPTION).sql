FROM TB_CLASS
LEFT JOIN TB_CLASS_PROFESSOR USING (CLASS_NO)
LEFT JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE CATEGORY = '예체능' AND PROFESSOR_NO IS NULL;

--14. 서반아어학과 학생이름과 지도교수 이름 출력
--(지도교수가 없는 학생이면 지도교수 미지정으로 표시, 고학번 학생이 먼저 표시)
SELECT STUDENT_NAME "학생이름", NVL(PROFESSOR_NAME,'지도교수 미지정')"지도 교수"
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
JOIN TB_PROFESSOR ON (PROFESSOR_NO = COACH_PROFESSOR_NO)
WHERE DEPARTMENT_NAME = '서반아어학과'
ORDER BY ENTRANCE_DATE;

--15. 휴학생이 아닌 학생 중 평점이 4.0 이상인 학생의 학번, 이름, 학과 이름, 평점
SELECT STUDENT_NO, STUDENT_NAME, DEPARTMENT_NAME, AVG(POINT)
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
JOIN TB_GRADE USING(STUDENT_NO)
WHERE ABSENCE_YN = 'N'
GROUP BY STUDENT_NO, STUDENT_NAME,DEPARTMENT_NAME
HAVING AVG(POINT) >= 4.0
ORDER BY 1;


--16. 환경조경학과 전공과목들의 과목 별 평점 
SELECT CLASS_NO, CLASS_NAME, AVG(POINT)
FROM TB_GRADE
JOIN TB_CLASS USING (CLASS_NO)
JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
GROUP BY CLASS_NO, CLASS_NAME, DEPARTMENT_NAME
HAVING DEPARTMENT_NAME = '환경조경학과';

--17. 최경희 학생과 같은 과 학생들의 이름과 주소 출력
SELECT STUDENT_NAME, STUDENT_ADDRESS
FROM TB_STUDENT
WHERE DEPARTMENT_NO = (SELECT DEPARTMENT_NO 
        FROM TB_STUDENT 
        WHERE STUDENT_NAME = '최경희');
        
--18. 국어국문학과에서 총 평점이 가장 높은 학생의 이름과 학번
SELECT STUDENT_NO, STUDENT_NAME
FROM (SELECT STUDENT_NO, STUDENT_NAME, AVG(POINT)
      FROM TB_GRADE
           JOIN TB_STUDENT USING(STUDENT_NO)
      WHERE DEPARTMENT_NO = (SELECT DEPARTMENT_NO
                             FROM TB_DEPARTMENT
                             WHERE DEPARTMENT_NAME = '국어국문학과')
    GROUP BY STUDENT_NO, STUDENT_NAME
    ORDER BY AVG(POINT) DESC)
WHERE ROWNUM =1;

    

